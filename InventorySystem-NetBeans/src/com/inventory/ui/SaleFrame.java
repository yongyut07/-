private void loadProductCombo() {
    cmbProducts.removeAllItems();

    ((javax.swing.JComboBox) cmbProducts).addItem("ทั้งหมด");

    for (com.inventory.model.Product p : ds.getProducts()) {
        ((javax.swing.JComboBox) cmbProducts).addItem(p);
    }
}
private void setupSaleTable() {
    tblSale.setModel(new DefaultTableModel(
        new Object[][]{},
        new String[]{"รหัสสินค้า", "ชื่อสินค้า", "จำนวน", "ราคาต่อหน่วย", "ราคารวม", "วันเวลา"}
    ));
}
private void loadSaleTable() {
    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) tblSale.getModel();
    model.setRowCount(0);

    Object selected = cmbProducts.getSelectedItem();

    for (com.inventory.model.TransactionRecord r : ds.getTransactions()) {
        if (r.getType() == com.inventory.model.TransactionType.SALE) {

            
            if (selected == null || selected.toString().equals("ทั้งหมด")) {
                model.addRow(new Object[]{
                    r.getProductId(),
                    r.getProductName(),
                    r.getQuantity(),
                    r.getUnitPrice(),
                    r.getTotalPrice(),
                    r.getDateTime()
                });
            } 
            
            else {
                com.inventory.model.Product p = (com.inventory.model.Product) selected;

                if (r.getProductId() == p.getId()) {
                    model.addRow(new Object[]{
                        r.getProductId(),
                        r.getProductName(),
                        r.getQuantity(),
                        r.getUnitPrice(),
                        r.getTotalPrice(),
                        r.getDateTime()
                    });
                }
            }
        }
    }
}
private void clearForm() {
    txtQuantity.setText("");
    cmbProducts.setSelectedIndex(0); 
}
