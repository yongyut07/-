    private void btnSaveSaleActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveSaleActionPerformed
       if (cmbProducts.getSelectedItem() == null) {
        JOptionPane.showMessageDialog(this, "กรุณาเลือกสินค้า");
        return;
    }

    try {
        Product selectedProduct = (Product) cmbProducts.getSelectedItem();
        int qty = Integer.parseInt(txtQuantity.getText().trim());

        if (qty <= 0) {
            JOptionPane.showMessageDialog(this, "จำนวนสินค้าต้องมากกว่า 0");
            return;
        }

        boolean success = ds.sellStock(selectedProduct.getId(), qty);

        if (success) {
            loadProductCombo();
            loadSaleTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "บันทึกการขายสำเร็จ");
        } else {
            JOptionPane.showMessageDialog(this, "ขายสินค้าไม่สำเร็จ เนื่องจากสต๊อกไม่พอ");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "กรุณากรอกจำนวนสินค้าเป็นตัวเลข");
    }
    }//GEN-LAST:event_btnSaveSaleActionPerformed

