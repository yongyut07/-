    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
         try {
        String name = txtName.getText().trim();
        String category = txtCategory.getText().trim();
        double costPrice = Double.parseDouble(txtCostPrice.getText().trim());
        double sellPrice = Double.parseDouble(txtSellPrice.getText().trim());
        int quantity = Integer.parseInt(txtQuantity.getText().trim());
        int minQuantity = Integer.parseInt(txtMinQuantity.getText().trim());

        if (name.isEmpty() || category.isEmpty()) {
            JOptionPane.showMessageDialog(this, "กรุณากรอกชื่อสินค้าและหมวดหมู่");
            return;
        }

        Product product = new Product(0, name, category, costPrice, sellPrice, quantity, minQuantity);
        ds.addProduct(product);

        loadProductTable();
        clearForm();
        JOptionPane.showMessageDialog(this, "เพิ่มสินค้าสำเร็จ");

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "กรุณากรอกราคาและจำนวนเป็นตัวเลข");
    }
    }//GEN-LAST:event_btnAddActionPerformed

    private void btnEditActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditActionPerformed
     if (selectedProductId == -1) {
        JOptionPane.showMessageDialog(this, "กรุณาเลือกสินค้าที่ต้องการแก้ไขจากตาราง");
        return;
    }

    try {
        String name = txtName.getText().trim();
        String category = txtCategory.getText().trim();
        double costPrice = Double.parseDouble(txtCostPrice.getText().trim());
        double sellPrice = Double.parseDouble(txtSellPrice.getText().trim());
        int quantity = Integer.parseInt(txtQuantity.getText().trim());
        int minQuantity = Integer.parseInt(txtMinQuantity.getText().trim());

        if (name.isEmpty() || category.isEmpty()) {
            JOptionPane.showMessageDialog(this, "กรุณากรอกชื่อสินค้าและหมวดหมู่");
            return;
        }

        Product product = new Product(selectedProductId, name, category, costPrice, sellPrice, quantity, minQuantity);
        boolean success = ds.updateProduct(product);

        if (success) {
            loadProductTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "แก้ไขสินค้าสำเร็จ");
        } else {
            JOptionPane.showMessageDialog(this, "ไม่พบสินค้าที่ต้องการแก้ไข");
        }

    } catch (NumberFormatException e) {
        JOptionPane.showMessageDialog(this, "กรุณากรอกราคาและจำนวนเป็นตัวเลข");
    }
    }//GEN-LAST:event_btnEditActionPerformed

