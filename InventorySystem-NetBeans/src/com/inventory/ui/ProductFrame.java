    private void btnDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnDeleteActionPerformed
        if (selectedProductId == -1) {
        JOptionPane.showMessageDialog(this, "กรุณาเลือกสินค้าที่ต้องการลบจากตาราง");
        return;
    }

    int confirm = JOptionPane.showConfirmDialog(
            this,
            "ต้องการลบสินค้านี้ใช่หรือไม่?",
            "ยืนยันการลบ",
            JOptionPane.YES_NO_OPTION
    );

    if (confirm == JOptionPane.YES_OPTION) {
        boolean success = ds.deleteProduct(selectedProductId);

        if (success) {
            loadProductTable();
            clearForm();
            JOptionPane.showMessageDialog(this, "ลบสินค้าสำเร็จ");
        } else {
            JOptionPane.showMessageDialog(this, "ไม่พบสินค้าที่ต้องการลบ");
        }
    }
    }//GEN-LAST:event_btnDeleteActionPerformed

    private void tblProductsMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblProductsMouseClicked
 int row = tblProducts.getSelectedRow();

    if (row >= 0) {
        selectedProductId = Integer.parseInt(tblProducts.getValueAt(row, 0).toString());

        txtName.setText(tblProducts.getValueAt(row, 1).toString());
        txtCategory.setText(tblProducts.getValueAt(row, 2).toString());
        txtCostPrice.setText(tblProducts.getValueAt(row, 3).toString());
        txtSellPrice.setText(tblProducts.getValueAt(row, 4).toString());
        txtQuantity.setText(tblProducts.getValueAt(row, 5).toString());
        txtMinQuantity.setText(tblProducts.getValueAt(row, 6).toString());
    }
    }//GEN-LAST:event_tblProductsMouseClicked
private void setupProductTable() {
    tblProducts.setModel(new javax.swing.table.DefaultTableModel(
        new Object[][]{},
        new String[]{
            "รหัสสินค้า",
            "ชื่อสินค้า",
            "หมวดหมู่",
            "ราคาทุน",
            "ราคาขาย",
            "จำนวนคงเหลือ",
            "ขั้นต่ำแจ้งเตือน"
        }
    ));
}

private void loadProductTable() {
    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel)  tblProducts.getModel();
    model.setRowCount(0);

    for (com.inventory.model.Product p : ds.getProducts()) {
        model.addRow(new Object[]{
            p.getId(),          
            p.getName(),        
            p.getCategory(),    
            p.getCostPrice(),  
            p.getSellPrice(),   
            p.getQuantity(),    
            p.getMinQuantity()  
        });
    }
}

private void clearForm() {
    txtName.setText("");
    txtCategory.setText("");
    txtCostPrice.setText("");
    txtSellPrice.setText("");
    txtQuantity.setText("");
    txtMinQuantity.setText("");
    selectedProductId = -1;
    tblProducts.clearSelection();
}
