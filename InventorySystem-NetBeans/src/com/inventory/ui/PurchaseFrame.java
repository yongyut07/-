    private void btnSavePurchaseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSavePurchaseActionPerformed
        String selectedName = (String) cmbProducts.getSelectedItem();

    if (selectedName == null || selectedName.equals("ทั้งหมด")) {
        javax.swing.JOptionPane.showMessageDialog(this, "กรุณาเลือกสินค้า");
        return;
    }

    try {
        int qty = Integer.parseInt(txtQuantity.getText().trim());
        double price = Double.parseDouble(txtPrice.getText().trim());

        if (qty <= 0 || price <= 0) {
            javax.swing.JOptionPane.showMessageDialog(this, "จำนวนและราคาต้องมากกว่า 0");
            return;
        }

        com.inventory.model.Product selectedProduct = null;
        for (com.inventory.model.Product p : ds.getProducts()) {
            if (p.getName().equals(selectedName)) {
                selectedProduct = p;
                break;
            }
        }

        if (selectedProduct == null) {
            javax.swing.JOptionPane.showMessageDialog(this, "ไม่พบสินค้า");
            return;
        }

        // สำคัญ: เรียกแค่ครั้งเดียว
        boolean success = ds.purchaseStock(selectedProduct.getId(), qty, price);

        if (success) {
            loadPurchaseTable();
            clearForm();
            javax.swing.JOptionPane.showMessageDialog(this, "บันทึกรับสินค้าเรียบร้อย");
        } else {
            javax.swing.JOptionPane.showMessageDialog(this, "ไม่สามารถบันทึกรับสินค้าได้");
        }

    } catch (NumberFormatException e) {
        javax.swing.JOptionPane.showMessageDialog(this, "กรุณากรอกจำนวนและราคาเป็นตัวเลข");
    }
    }//GEN-LAST:event_btnSavePurchaseActionPerformed

