private void loadProductCombo() {
    cmbProducts.removeAllItems();
    cmbProducts.addItem("ทั้งหมด");

    for (com.inventory.model.Product p : ds.getProducts()) {
        cmbProducts.addItem(p.getName());
    }
}

private void setupPurchaseTable() {
    tblPurchase.setModel(new javax.swing.table.DefaultTableModel(
        new Object[][]{},
        new String[]{"รหัสสินค้า", "ชื่อสินค้า", "จำนวน", "ราคาต่อหน่วย", "ราคารวม", "วันเวลา"}
    ));
}

private void loadPurchaseTable() {
    javax.swing.table.DefaultTableModel model =
            (javax.swing.table.DefaultTableModel) tblPurchase.getModel();

    model.setRowCount(0); // สำคัญ: ล้างตารางก่อนทุกครั้ง

    String selected = (String) cmbProducts.getSelectedItem();

    for (com.inventory.model.TransactionRecord r : ds.getTransactions()) {
        if (r.getType() == com.inventory.model.TransactionType.PURCHASE) {

            if (selected == null || selected.equals("ทั้งหมด")) {
                model.addRow(new Object[]{
                    r.getProductId(),
                    r.getProductName(),
                    r.getQuantity(),
                    r.getUnitPrice(),
                    r.getTotalPrice(),
                    r.getDateTime()
                });
            } else if (r.getProductName().equals(selected)) {
                model.addRow(new Object[]{
                    r.getProductId(),
                    r.getProductName(),
                    r.getQuantity(),
                    r.getUnitPrice(),
                    r.getTotalPrice(),
                    r.getDateTime()
                });
            }
        }
    }
}

private void clearForm() {
    txtQuantity.setText("");
    txtPrice.setText("");
    cmbProducts.setSelectedIndex(0);
}
